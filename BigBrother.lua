local addonName = BigBrother.name

local function parseGender(string)
    return string.sub(string, 1, -4)
end

local function findIndex(table, value)
    for k, v in pairs(table) do
        if v == value then
            return k
        end
    end
    return nil
end

local function track_EVENT_GUILD_MEMBER_CHARACTER_ZONE_CHANGED(event, guildId, displayName, characterName, zoneName)
    local timeStamp = GetTimeStamp()
    local characterName = parseGender(characterName)
    
    local index = findIndex(BigBrotherData, zoneName)
    if index then
        zoneId = index
        if GetZoneNameById(zoneId) ~= zoneName then return end
    end

    if not BigBrother.savedVariables[displayName] then BigBrother.savedVariables[displayName] = {} end
    if not BigBrother.savedVariables[displayName][characterName] then BigBrother.savedVariables[displayName][characterName] = {} end
    BigBrother.savedVariables[displayName][characterName][timeStamp] = zoneId
end

